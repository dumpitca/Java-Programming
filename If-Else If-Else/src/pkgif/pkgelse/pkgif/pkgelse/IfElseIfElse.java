public class IfElseIfElse {

    public static void displayFalse() {
        System.out.println("The statements are false");
    }
    
   
   
    public static void main(String[] args) {
        int number1 = 28;
        int number2 = 28;
        
        if (number1 == number2) {
            System.out.println("The IF statement will be printed"); //If the first IF statement is true this lne will be printed 
       
        }else {
               displayFalse(); 
        
        } 
        
        
        if (number1 >= 27) {
            System.out.println("Number1 is greater than or equal to 27");
        
        }
    }
    
}
